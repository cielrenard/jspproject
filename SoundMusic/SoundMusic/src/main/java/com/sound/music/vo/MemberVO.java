package com.sound.music.vo;

//이 클래스는 회원처리를 위한 데이터를 기억할 VO 클래스
public class MemberVO {

	private String id;
	private String pw;
	
	
	public String getId() {
		return id;
	}
	public void setId(String id) {
		this.id = id;
	}
	public String getPw() {
		return pw;
	}
	public void setPw(String pw) {
		this.pw = pw;
	}
	
	
}
